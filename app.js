// **Описание**: Создайте программу, которая определяет статус заказа в интернет-магазине на основе его номера.
//
// **Входные данные**: Переменная orderNumber с числовым значением номера заказа
//
// **Выходные данные**: Сообщение в консоль о статусе заказа:
// - Если номер заказа 1001: "Заказ обрабатывается"
// - Если номер заказа 1002: "Заказ отправлен"
// - Если номер заказа 1003: "Заказ доставлен"
// - Если номер заказа 1004: "Заказ отменен"
// - Для любого другого номера: "Заказ не найден"
//
// **Ограничения**: Номер заказа - положительное целое число
//
// **Примеры**:
// Input: orderNumber = 1001
// Output: Заказ обрабатывается
//
// Input: orderNumber = 1003
// Output: Заказ доставлен
//
// Input: orderNumber = 9999
// Output: Заказ не найден

const orderNumber = 1002;

switch (orderNumber) {
  case 1001: 
    console.log('Заказ обрабатывается');
    break;
  case 1002: 
    console.log('Заказ отправлен');
    break;
  case 1003: 
    console.log('Заказ доставлен');
    break;
  case 1004: 
    console.log('Заказ отменен');
    break;
  default:
    console.log('Заказ не найден');
}